class Beer{
	public static void beerName(String beName){
		System.out.println(beName);
	}
	public static void beerQuantity(int beQuantity){
		System.out.println(beQuantity);
	}
	public static void alcoholPercentage(float percentage){
		System.out.println(percentage);
	}
	public static void beerPrice(String Name,int price){
		System.out.println(Name);
		System.out.println(price);
	}
	public static void beerType(boolean strong,int price){
		System.out.println(strong);
		System.out.println(price);
	}
	public static void beerOrder(String name,int quantity ,int price){
		System.out.println(name);
		System.out.println(quantity);
		System.out.println(price);
	}
	public static void beerBrand(String BrandName,int price ,int quantity){
		System.out.println(BrandName);
		System.out.println(price);
		System.out.println(quantity);
	}
}